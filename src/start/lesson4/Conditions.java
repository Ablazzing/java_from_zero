package start.lesson4;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;

        if (x > y) {
            System.out.println("Х больше чем Y");
        } else if (x == y) {
            System.out.println("X равен Y");
        } else {
            System.out.println("Y больше X");
        }
    }
}
